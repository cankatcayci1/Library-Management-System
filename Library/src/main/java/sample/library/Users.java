public class Users {

    private int userId;
    private String name;
    private String surname;
    private String userName;
    private String password;

    public Users(int userId,String name,String surname,String userName,String password)
    {
        this.userId = userId;
        this.name = name;
        this.surname = surname;
        this.userName = userName;
        this.password = password;
    }



}
